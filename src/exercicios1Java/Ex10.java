package exercicios1Java;

public class Ex10 {
    public static void main(String[] args) {
        int saldo = 500;
        System.out.println("Saldo = " + saldo);
        saldo += 100;
        System.out.println("Saldo + 100: " + saldo);
        saldo -= 50;
        System.out.println("Saldo - 50: " + saldo);
        saldo *= 2;
        System.out.println("Saldo * 2: " + saldo);
    }
}